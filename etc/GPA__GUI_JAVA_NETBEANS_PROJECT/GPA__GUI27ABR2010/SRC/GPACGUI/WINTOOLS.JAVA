    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        lines = 0;
        jButton2.setEnabled(false);
        reader = null;
        try {
            reader = new FileReader(new File(filePath));
            leitor = new BufferedReader(reader);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while ((linha = leitor.readLine()) != null) {
                lines++;
            }
        } catch (IOException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        int j = (int) Math.sqrt(lines); // -----------------------------------------
        int r = 0, s = 0;
        double matrix[][] = new double[j][j];
        try {
            reader = new FileReader(new File(filePath));
            leitor = new BufferedReader(reader);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            file = new FileOutputStream(destiny, false);
            dados = new DataOutputStream(new BufferedOutputStream(file));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while ((linha = leitor.readLine()) != null) {
                double d = Double.parseDouble(linha.toString());
                matrix[r][s++] = d;
                if (s < j) {
                    dados.writeBytes(linha + " ");
                } else {
                    dados.writeBytes(linha);
                    if (r == (j - 1) & s == j) {
                        break;
                    }
                    s = 0;
                    r++;
                    dados.writeBytes("\n");
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            leitor.close();
            reader.close();
        } catch (IOException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            dados.close();
            file.close();
        } catch (IOException ex) {
            Logger.getLogger(Wintools.class.getName()).log(Level.SEVERE, null, ex);
        }
        jButton3.setEnabled(true);
    }//GEN-LAST:event_jButton2ActionPerformed

